package com.example.demo1.models.enums;

/**
 * Enum que define los posibles estados de una oportunidad laboral o creativa.
 */
public enum EstadoOportunidad {

    /**
     * La oportunidad está activa y acepta postulaciones.
     */
    ABIERTO,

    /**
     * La oportunidad ya no está disponible.
     */
    CERRADO
}
